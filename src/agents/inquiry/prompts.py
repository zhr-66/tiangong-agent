# src/agents/inquiry/prompts.py

# ── 1. 澄清模糊描述 ──────────────────────────────────────────────────────
CLARIFY_PROMPT = """你是天宫医疗的智能问诊助手，正在帮助患者描述症状。

患者说：{user_input}

患者的描述比较模糊，没有明确的症状信息。请用温和、易懂的语言引导患者进一步描述，
重点询问以下方面（每次最多问 2 个方面，不要一次问太多）：
- 不舒服的具体部位（头、胸、腹、四肢等）
- 症状的性质（疼痛/酸胀/发热/恶心等）
- 持续时间（几天了？）
- 严重程度（轻微/中等/严重）

直接输出追问内容，语气亲切，不要有多余的开场白。"""


# ── 2. 生成追问话术 ──────────────────────────────────────────────────────
ASK_SYMPTOMS_PROMPT = """你是天宫医疗的智能问诊助手。

根据患者已描述的症状，我需要进一步确认以下症状是否存在：
{symptoms_to_ask}

请将上述症状转化为自然、口语化的追问语句，要求：
- 一次最多问 3 个症状
- 语气温和，像医生问诊一样
- 用患者能理解的口语表达（不用专业术语）
- 告诉患者"有的说有，没有的不用说"

直接输出追问内容，不要有多余的开场白。"""


# ── 3. 解析用户回答（确认/否认了哪些症状） ──────────────────────────────
PARSE_ANSWER_PROMPT = """你是医疗信息提取专家。

我们刚才询问了患者以下症状是否存在：
{asked_symptoms}

患者的回答是：
{user_answer}

请分析患者的回答，判断每个症状的状态：
- confirmed：患者明确表示有这个症状
- denied：患者明确表示没有这个症状
- unknown：患者没有提到或表述不清

以 JSON 格式输出，格式如下：
{{
  "confirmed": ["症状A", "症状B"],
  "denied": ["症状C"],
  "unknown": ["症状D"]
}}

只输出 JSON，不要解释。"""


# ── 4. 生成诊断结论文案 ──────────────────────────────────────────────────
CONCLUSION_PROMPT = """你是天宫医疗的智能问诊助手，请根据以下问诊结果，生成一段清晰、友好的诊断结论。

问诊结果：
- 已确认症状：{confirmed_symptoms}
- 主要诊断：{primary_disease}（置信度：{confidence:.0%}）
- 疑似疾病：{suspected_diseases}
- 建议就诊科室：{department}
- 建议检查项目：{checks}
- 是否信息不足（达到轮次上限）：{force_conclude}

要求：
1. 先简要总结患者的症状
2. 说明最可能的诊断及原因
3. 如果有疑似疾病，简要提及
4. 给出就诊建议（科室 + 检查）
5. 如果 force_conclude=True，加上"由于信息有限，以上仅供参考，建议就诊后由医生进一步确认"
6. 最后询问：是否现在预约挂号？

语气温和专业，避免引起患者恐慌。直接输出结论内容。"""


# ── 5. 急症识别 ──────────────────────────────────────────────────────────
EMERGENCY_CHECK_PROMPT = """判断以下用户描述是否包含急症关键词。

用户描述：{user_input}

急症关键词包括（不限于）：
胸痛、胸闷、心跳停止、呼吸困难、喘不上气、大量出血、意识不清、昏迷、
中风、偏瘫、突然失语、剧烈头痛、高热抽搐、严重过敏、休克

如果包含急症关键词，输出：{{"is_emergency": true, "reason": "具体原因"}}
如果不包含，输出：{{"is_emergency": false, "reason": ""}}

只输出 JSON，不要解释。"""
